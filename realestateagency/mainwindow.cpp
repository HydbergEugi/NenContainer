// Используемые переменные:
// user - информация о текущем пользователе.
//
// Используемые функции:
// MainWindow(QWidget *parent) - инициализация формы;
// ~MainWindow - удаление формы;
// autorizationMessage() - слот завершения регистрации;
// on_Real_estate_clicked() - кнопка объекта недвижимости;
// on_Agreements_clicked() - кнопка договора;
// on_Users_clicked() - кнопка пользователя;
// on_locality_clicked() - кнопка параметра;
// on_floor_covering_clicked() - кнопка параметра.

#include "mainwindow.h"
#include "ui_mainwindow.h"
#include <QMessageBox>
#include <QTableView>
#include "users.h"

extern currentUser user;

// /////////////////////////////////////////////////////////////////////////////////////////
// MainWindow(QWidget *parent) - инициализация формы.
//
// Формальные параметры:
// *parent - ссылка на родительское окно.
//
// Локальные переменные:
// ui - переменная пользовательского интерфейса;
// model - модель таблицы;
// /////////////////////////////////////////////////////////////////////////////////////////
MainWindow::MainWindow(QWidget *parent) :
    QMainWindow(parent),
    ui(new Ui::MainWindow)
{
    ui->setupUi(this);
}
// /////////////////////////////////////////////////////////////////////////////////////////
// ~MainWindow() - удаление формы.
//
// Локальные переменные:
// ui - переменная пользовательского интерфейса.
// /////////////////////////////////////////////////////////////////////////////////////////
MainWindow::~MainWindow()
{
    delete ui;
}

// /////////////////////////////////////////////////////////////////////////////////////////
// autorizationMessage() - слот завершения регистрации.
//
// Локальные переменные:
// user - информация о текущем пользователе;
// ui - переменная пользовательского интерфейса.
// /////////////////////////////////////////////////////////////////////////////////////////

void MainWindow::autorizationMessage()
{
    if(user.getUserName() != 0)
    {
    ui->Users->hide();
    ui->locality->hide();
    ui->floor_covering->hide();
    }
     show();
}
// /////////////////////////////////////////////////////////////////////////////////////////
// on_Real_estate_clicked() - кнопка объекта недвижимости.
// /////////////////////////////////////////////////////////////////////////////////////////
void MainWindow::on_Real_estate_clicked()
{
    emit real_estate_open();
}
// /////////////////////////////////////////////////////////////////////////////////////////
// on_Agreements_clicked() - кнопка договора.
// /////////////////////////////////////////////////////////////////////////////////////////
void MainWindow::on_Agreements_clicked()
{
    emit purchases_open();
}
// /////////////////////////////////////////////////////////////////////////////////////////
// on_Users_clicked() - кнопка пользователя.
// /////////////////////////////////////////////////////////////////////////////////////////
void MainWindow::on_Users_clicked()
{
    emit users_open();
}
// /////////////////////////////////////////////////////////////////////////////////////////
// on_locality_clicked() - кнопка параметра.
// /////////////////////////////////////////////////////////////////////////////////////////
void MainWindow::on_locality_clicked()
{
    emit locality_open();
}
// /////////////////////////////////////////////////////////////////////////////////////////
// on_floor_covering_clicked() - кнопка параметра.
// /////////////////////////////////////////////////////////////////////////////////////////
void MainWindow::on_floor_covering_clicked()
{
    emit floor_covering_open();
}

